    // Fun√ß√£o ass√≠ncrona para upload de imagem nas APIs
    async function uploadImage(imageFile) {
        if (!imageFile) {
            console.warn('Nenhum arquivo de imagem selecionado.');
            return null;
        }

        const formData = new FormData();
        
        for (const config of IMG_API_CONFIGS) {
            try {
                updateLoading(30, `Fazendo upload da imagem usando ${config.name}...`);
                console.log(`Tentando upload com a API: ${config.name}`);

                const formData = new FormData();
                formData.append('key', config.key);
                formData.append('image', imageFile);

                const response = await fetch(config.endpoint, {
                    method: 'POST',
                    body: formData
                });

                if (!response.ok) {
                    console.warn(`Falha no upload com a API ${config.name}. Status: ${response.status}. Tentando a pr√≥xima.`);
                    continue;
                }
                
                const data = await response.json();
                console.log(`Resposta JSON da API ${config.name}:`, data);

                let imageUrl = null;
                if (data.success) {
                    imageUrl = data.data.url;
                }

                if (imageUrl) {
                    updateLoading(60, `Imagem enviada com sucesso. Publicando no mural...`);
                    return imageUrl;
                } else {
                    console.warn(`A API ${config.name} n√£o retornou uma URL v√°lida. Tentando a pr√≥xima.`);
                    continue;
                }
            } catch (error) {
                console.warn(`Erro de comunica√ß√£o com a API ${config.name}.`, error);
                continue;
            }
        }
        
        // Se todas as APIs falharem, atualiza o status de carregamento antes de retornar null
        updateLoading(100, 'Falha no upload da imagem.');
        console.error('Todas as chaves de API falharam no upload.');
        return null;
    }
    
    // Fun√ß√£o para formatar a data para o formato dd-mm-aaaa
